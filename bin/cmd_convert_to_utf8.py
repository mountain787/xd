def has_garbled(text):
    """检查文本是否包含乱码"""
    for pattern in GARBLED_MAPPING.keys():
        if pattern in text:
            return True
    return False

def convert_file(filepath, dry_run=False):
    """转换单个文件"""
    try:
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()

        original = content
        # 按长度降序排序，先替换长的字符串
        for bad, good in sorted(GARBLED_MAPPING.items(), key=lambda x: -len(x[0])):
            content = content.replace(bad, good)

        if content != original:
            if dry_run:
                print(f"[DRY RUN] Would convert: {filepath}")
                return True
            else:
                with open(filepath, 'w', encoding='utf-8') as f:
                    f.write(content)
                print(f"Converted: {filepath}")
                return True
        return False
    except Exception as e:
        print(f"Error processing {filepath}: {e}")
        return False

def main():
    parser = argparse.ArgumentParser(description='修复 Pike 文件中的 UTF-8 中文乱码')
    parser.add_argument('path', nargs='?', default='gamelib/cmds',
                       help='要扫描的目录或文件路径 (默认: gamelib/cmds)')
    parser.add_argument('-n', '--dry-run', action='store_true',
                       help='只检查不修改文件')
    parser.add_argument('-v', '--verbose', action='store_true',
                       help='显示详细信息')
    parser.add_argument('--pattern', default='*.pike',
                       help='文件匹配模式 (默认: *.pike)')

    args = parser.parse_args()

    path = args.path
    dry_run = args.dry_run

    # 确定是文件还是目录
    if os.path.isfile(path):
        files = [path]
    elif os.path.isdir(path):
        files = glob.glob(os.path.join(path, '**', args.pattern), recursive=True)
    else:
        print(f"错误: 路径不存在: {path}")
        return 1

    print(f"扫描 {len(files)} 个文件...")
    if dry_run:
        print("DRY RUN 模式: 不会修改任何文件\n")

    converted = []
    checked = 0

    for f in files:
        checked += 1
        if args.verbose:
            print(f"检查: {f}")
        if convert_file(f, dry_run):
            converted.append(f)

    print(f"\n检查了 {checked} 个文件")
    if converted:
        print(f"发现 {len(converted)} 个需要转换的文件")
        if dry_run:
            print("\n使用 --no-dry-run 或去掉 -n 参数来实际转换文件")
    else:
        print("没有发现需要转换的文件")

    return 0

if __name__ == '__main__':
    exit(main())
